class PG_Colors {
 typedef STL_MAP<const char*, SDL_Color> colormap;
 typedef STL_MAP<const char*, SDL_Color>::iterator cit;

 public:
   PG_Colors() { if (!PG_Colors::initialized) {PG_Colors::InitColors(); PG_Colors::initialized = true;}}
   static bool FindColorByName(const char *name, SDL_Color &c) {
       cit col = PG_Colors::colors.find(name);
       if (col != PG_Colors::colors.end()) {
           c = colors[name];
           return true;
       }
       return false;
   }

  static const SDL_Color FindColorByName(const char *name) {
	SDL_Color c;
	if(FindColorByName(name, c)) {
		return c;
	}
       return colors["black"];
   }
   static void InitColors();

   const SDL_Color operator[](char *name) {
	return FindColorByName(static_cast<const char*>(name));
   }
   
   const SDL_Color operator[](const char *name) {
        return FindColorByName(name);
   }
   
 protected:
   static colormap colors;
   static bool     initialized;
};
